function Gv = G_1(zeta)
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
% Gv = G_1(zeta) - jump map of H_1
%
%  zeta = (z,R,y)
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
    z = zeta(1:9);
    R  = reshape(zeta(10:18),[3 3]);
    Gv = [z;R(:);minV(R'*rho(z))];