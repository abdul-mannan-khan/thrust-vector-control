function v = D_1(zeta)
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
% v = D_1(zeta) - jump set of H_1
%
%  zeta = (z,R,y)
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
    z = zeta(1:9);
    R = reshape(zeta(10:18),[3 3]);
    y = zeta(19:21);
    v = D(R'*rho(z),y);